# ========================================================
# Seção 14 - Estruturas de Repetição (while, each)
# ========================================================

# --------------------------------------------------------
# While
# --------------------------------------------------------

i = 0
num =  5

while i<=num
  puts "contando. ... " + i.to_s
  i+=1
end

# --------------------------------------------------------
# Each
# --------------------------------------------------------

(0..5).each do |i|
  puts "O valor lido foi:" + i.to_s
end

# exemplo com array

['A','b',3].each do |item|
  puts "O valor lido foi:" + item.to_s
end